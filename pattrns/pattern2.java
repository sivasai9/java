

public class pattern2 {
    public static void main(String[] args) {
        patt(5);
        
    }

    static void patt(int n){
        for(int i=1;i<=n;i++){
            for(int j=1;j<=n;j++){
                System.out.print(4);
            }
            System.out.println();
        }
    }
    
}
